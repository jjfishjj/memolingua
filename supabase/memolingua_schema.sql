  FOR ALL USING (auth.uid() = user_id) WITH CHECK (auth.uid() = user_id);

-- ============================================================
-- RPC：三源融合（SECURITY DEFINER 以便寫入 vark_profiles）
-- 權重：quiz 30% + behavior 40% + eeg 30%；缺的來源自動重新分配權重。
-- ============================================================
CREATE OR REPLACE FUNCTION public.refresh_vark_profile(p_user_id uuid)
RETURNS void
LANGUAGE plpgsql SECURITY DEFINER SET search_path = public
AS $$
DECLARE
  p public.vark_profiles%ROWTYPE;
  qs numeric; bs numeric; es numeric;       -- 各來源總和
  wq numeric := 0; wb numeric := 0; we numeric := 0; wtot numeric;
  fv numeric; fa numeric; fr numeric; fk numeric;  -- 各型融合分數
  nsrc int := 0;
  bestval numeric; besttype text;
BEGIN
  SELECT * INTO p FROM public.vark_profiles WHERE user_id = p_user_id;
  IF NOT FOUND THEN RETURN; END IF;

  qs := p.quiz_v + p.quiz_a + p.quiz_r + p.quiz_k;
  bs := p.behavior_v + p.behavior_a + p.behavior_r + p.behavior_k;
  es := p.eeg_v + p.eeg_a + p.eeg_r + p.eeg_k;

  IF qs > 0 THEN wq := 0.30; nsrc := nsrc + 1; END IF;
  IF bs > 0 THEN wb := 0.40; nsrc := nsrc + 1; END IF;
  IF es > 0 THEN we := 0.30; nsrc := nsrc + 1; END IF;
  wtot := wq + wb + we;

  IF wtot = 0 THEN
    -- 完全無資料：平均
    fv := 0.25; fa := 0.25; fr := 0.25; fk := 0.25;
  ELSE
    fv := (CASE WHEN qs>0 THEN wq*p.quiz_v/qs ELSE 0 END
         + CASE WHEN bs>0 THEN wb*p.behavior_v/bs ELSE 0 END
         + CASE WHEN es>0 THEN we*p.eeg_v/es ELSE 0 END) / wtot;
    fa := (CASE WHEN qs>0 THEN wq*p.quiz_a/qs ELSE 0 END
         + CASE WHEN bs>0 THEN wb*p.behavior_a/bs ELSE 0 END
         + CASE WHEN es>0 THEN we*p.eeg_a/es ELSE 0 END) / wtot;
    fr := (CASE WHEN qs>0 THEN wq*p.quiz_r/qs ELSE 0 END
         + CASE WHEN bs>0 THEN wb*p.behavior_r/bs ELSE 0 END
         + CASE WHEN es>0 THEN we*p.eeg_r/es ELSE 0 END) / wtot;
    fk := (CASE WHEN qs>0 THEN wq*p.quiz_k/qs ELSE 0 END
         + CASE WHEN bs>0 THEN wb*p.behavior_k/bs ELSE 0 END
         + CASE WHEN es>0 THEN we*p.eeg_k/es ELSE 0 END) / wtot;
  END IF;

  -- 主型 = 最大值
  bestval := fv; besttype := 'visual';
  IF fa > bestval THEN bestval := fa; besttype := 'auditory'; END IF;
  IF fr > bestval THEN bestval := fr; besttype := 'reading'; END IF;
  IF fk > bestval THEN bestval := fk; besttype := 'kinesthetic'; END IF;

  UPDATE public.vark_profiles SET
    blended_v = round(fv*100, 2), blended_a = round(fa*100, 2),
    blended_r = round(fr*100, 2), blended_k = round(fk*100, 2),
    dominant_type = besttype,
    confidence = least(0.95, 0.16 + 0.24*nsrc),   -- 2源≈0.64、3源≈0.88
    updated_at = now()
  WHERE user_id = p_user_id;
END;
$$;

-- 測驗結束 → 統計該 session 作答為 quiz_*，upsert 後重算融合。
CREATE OR REPLACE FUNCTION public.calculate_vark_quiz(p_session_id uuid, p_user_id uuid)
RETURNS void
LANGUAGE plpgsql SECURITY DEFINER SET search_path = public
AS $$
DECLARE cv int; ca int; cr int; ck int;
BEGIN
  SELECT
    count(*) FILTER (WHERE selected_type='visual'),
    count(*) FILTER (WHERE selected_type='auditory'),
    count(*) FILTER (WHERE selected_type='reading'),
    count(*) FILTER (WHERE selected_type='kinesthetic')
  INTO cv, ca, cr, ck
  FROM public.vark_responses
  WHERE session_id = p_session_id AND user_id = p_user_id;

  INSERT INTO public.vark_profiles (user_id, quiz_v, quiz_a, quiz_r, quiz_k)
  VALUES (p_user_id, cv, ca, cr, ck)
  ON CONFLICT (user_id) DO UPDATE
    SET quiz_v = EXCLUDED.quiz_v, quiz_a = EXCLUDED.quiz_a,
        quiz_r = EXCLUDED.quiz_r, quiz_k = EXCLUDED.quiz_k,
        updated_at = now();

  PERFORM public.refresh_vark_profile(p_user_id);
END;
$$;

-- 由近 7 天 EEG 找出平均專注度最高的時段。
CREATE OR REPLACE FUNCTION public.compute_best_learning_hour(p_user_id uuid)
RETURNS void
LANGUAGE plpgsql SECURITY DEFINER SET search_path = public
AS $$
DECLARE best_hour int;
BEGIN
  SELECT EXTRACT(HOUR FROM recorded_at)::int INTO best_hour
  FROM public.eeg_sessions
  WHERE user_id = p_user_id
    AND recorded_at >= now() - interval '7 days'
    AND engagement IS NOT NULL
  GROUP BY 1
  ORDER BY avg(engagement) DESC
  LIMIT 1;

  IF best_hour IS NOT NULL THEN
    UPDATE public.vark_profiles SET best_learning_hour = best_hour, updated_at = now()
    WHERE user_id = p_user_id;
  END IF;
END;
$$;

GRANT EXECUTE ON FUNCTION public.refresh_vark_profile(uuid)        TO authenticated, service_role;
GRANT EXECUTE ON FUNCTION public.calculate_vark_quiz(uuid, uuid)   TO authenticated, service_role;
GRANT EXECUTE ON FUNCTION public.compute_best_learning_hour(uuid)  TO authenticated, service_role;

