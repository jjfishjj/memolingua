-- ============================================================
-- MemoLingua · 核心 Schema 遷移
-- VARK 三源融合 + FSRS 記憶 + EEG 腦波
--
-- 用法：Supabase → SQL Editor → 貼上整段執行一次。
-- 安全：可重複執行（IF NOT EXISTS / CREATE OR REPLACE / DROP POLICY IF EXISTS）。
-- 前提：auth.users 與 conversations 已存在（本專案已有）。
-- 跑完後再跑 seed_vark_questions.sql 匯入題庫。
-- ============================================================

-- ---------- 1. vark_questions（題庫，公開可讀） ----------
CREATE TABLE IF NOT EXISTS public.vark_questions (
  id            integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  question_text text NOT NULL,
  context       text,
  option_v      text NOT NULL,
  option_a      text NOT NULL,
  option_r      text NOT NULL,
  option_k      text NOT NULL,
  category      text,
  language      text NOT NULL DEFAULT 'zh',
  is_active     boolean NOT NULL DEFAULT true,
  created_at    timestamptz NOT NULL DEFAULT now()
);

-- ---------- 2. vark_responses（測驗作答） ----------
CREATE TABLE IF NOT EXISTS public.vark_responses (
  id            uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id       uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  question_id   integer REFERENCES public.vark_questions(id) ON DELETE SET NULL,
  selected_type text NOT NULL CHECK (selected_type IN ('visual','auditory','reading','kinesthetic')),
  session_id    uuid NOT NULL,
  created_at    timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_vark_responses_session ON public.vark_responses(session_id);
CREATE INDEX IF NOT EXISTS idx_vark_responses_user ON public.vark_responses(user_id);

-- ---------- 3. vark_profiles（三源融合輪廓，每人一列） ----------
CREATE TABLE IF NOT EXISTS public.vark_profiles (
  user_id            uuid PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  quiz_v numeric NOT NULL DEFAULT 0, quiz_a numeric NOT NULL DEFAULT 0,
  quiz_r numeric NOT NULL DEFAULT 0, quiz_k numeric NOT NULL DEFAULT 0,
  behavior_v numeric NOT NULL DEFAULT 0, behavior_a numeric NOT NULL DEFAULT 0,
  behavior_r numeric NOT NULL DEFAULT 0, behavior_k numeric NOT NULL DEFAULT 0,
  eeg_v numeric NOT NULL DEFAULT 0, eeg_a numeric NOT NULL DEFAULT 0,
  eeg_r numeric NOT NULL DEFAULT 0, eeg_k numeric NOT NULL DEFAULT 0,
  blended_v numeric NOT NULL DEFAULT 0, blended_a numeric NOT NULL DEFAULT 0,
  blended_r numeric NOT NULL DEFAULT 0, blended_k numeric NOT NULL DEFAULT 0,
  dominant_type      text,
  confidence         numeric,
  conversation_count integer NOT NULL DEFAULT 0,
  avg_cognitive_load numeric,
  best_learning_hour integer,
  updated_at         timestamptz NOT NULL DEFAULT now()
);

-- ---------- 4. eeg_sessions（腦波匯總） ----------
CREATE TABLE IF NOT EXISTS public.eeg_sessions (
  id               uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id          uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  conversation_id  uuid,
  device           text,
  duration_sec     numeric,
  delta numeric, theta numeric, alpha numeric, beta numeric, gamma numeric,
  engagement       numeric,
  cognitive_load   numeric,
  encoding_strength numeric,
  relaxation       numeric,
  eeg_v numeric, eeg_a numeric, eeg_r numeric, eeg_k numeric,
  signal_quality   numeric,
  recorded_at      timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_eeg_sessions_user_time ON public.eeg_sessions(user_id, recorded_at DESC);

-- ---------- 5. memory_items（FSRS 記憶卡） ----------
CREATE TABLE IF NOT EXISTS public.memory_items (
  id               uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id          uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  content          text NOT NULL,
  meaning          text,
  encoding_context text,           -- visual / auditory / reading / kinesthetic
  fsrs_stability   numeric NOT NULL DEFAULT 0,
  fsrs_difficulty  numeric NOT NULL DEFAULT 0,
  fsrs_state       text NOT NULL DEFAULT 'new',
  lapses           integer NOT NULL DEFAULT 0,
  repetitions      integer NOT NULL DEFAULT 0,
  next_review_at   timestamptz NOT NULL DEFAULT now(),
  last_reviewed_at timestamptz,
  created_at       timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_memory_items_due ON public.memory_items(user_id, next_review_at);

-- ---------- 6. memory_reviews（複習紀錄） ----------
CREATE TABLE IF NOT EXISTS public.memory_reviews (
  id              uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  memory_item_id  uuid NOT NULL REFERENCES public.memory_items(id) ON DELETE CASCADE,
  user_id         uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  quality         integer NOT NULL CHECK (quality BETWEEN 1 AND 4),
  review_modality text,
  eeg_engagement  numeric,
  created_at      timestamptz NOT NULL DEFAULT now()
);

